const wss = require('../../index');
const net = require('net');
const readline = require('readline');
const { arregloTagsApendice, arregloTagsApendiceTCP, arregloTagsApendiceAntenas, arregloTagsApendiceGeneral } = require('../../helpers/chanduy/arregloTags');


let interval = null; 
let dataCache = {
    Apendice: [],
    ApendiceGral: [],
    ApendiceTCP: [],
    ApendiceAntenas: []
}; 

let completedFuntions = 0
const clientSubscriptions = new Map()

const checkAllFuntionsComplete = () => {
    if (completedFuntions === 4) {
        broadcastData()
        completedFuntions = 0
    }
}

const broadcastData = () => {
    clientSubscriptions.forEach((isSubscribed, client) => {
        if (isSubscribed) {
            //console.log("Sending data: ", JSON.stringify(dataCache))
            client.send(JSON.stringify(dataCache));
        }
    })

}

